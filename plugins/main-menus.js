const FOTO_MENU = 'https://causas-files.vercel.app/fl/pxun.jpg'
const comandos = menuConfig.map(m => m.cmd)

const handler = async (m, { conn, command }) => {
  const key = command.toLowerCase()
  const menu = menuConfig.find(m => m.cmd === key)
  if (!menu) return
  const caption = `Hola, Soy *Yotsuba.* Este es el menú *${menu.name}*.\n\n${principio}\n${menu.text}\n${dev}`
  await conn.sendMessage(m.chat, {
    image: { url: FOTO_MENU },
    caption
  }, { quoted: m })
}

handler.help = comandos
handler.tags = ['menu']
handler.command = new RegExp(`^(${comandos.join('|')})$`, "i")

export default handler